<template>
  <v-dialog
    v-model="show"
    scrollable
    persistent
    max-width="500px"
    eager
  >
    <v-card>
      <v-card-title>{{ titleText }}</v-card-title>
      <v-divider/>
      <v-card-text>
        <v-form ref="form" v-model="valid">
          <v-select
            label="申請種別"
            v-model="requestType"
            :items="items"
            :rules="requestTypeRules"
            return-object
          />

          <v-text-field
            label="タイトル"
            v-model="title"
            :rules="titleRules"
          />

          <!-- 選択した申請書ごとに項目を出し分けする -->
          <!-- 休暇申請 -->
          <div v-if="requestType.value == 'paid_leave'">
            <!-- 子コンポーネントを挿入する -->
            <ItemDialogCreateRequestFormPaidLeave ref="ItemDialogCreateRequestFormPaidLeave"/>
            <p>{{ message }}</p>
          </div>

          <!-- 備品申請 -->
          <div v-else-if="requestType.value == 'equipment'">
            <v-textarea
              label="商品名"
              v-model="itemName"
              :rules="itemNameRules"
              rows="3"
            />

            <v-textarea
              label="購入理由"
              v-model="reason"
              :rules="reasonRules"
              rows="3"
            />

            <v-textarea
              label="備考"
              v-model="memo"
              rows="3"
            />
          </div>

        </v-form>
      </v-card-text>
      <v-divider/>
      <v-card-actions>
        <v-spacer/>
        <v-btn
          color="grey darken-1"
          text
          @click="onClickClose"
        >
          キャンセル
        </v-btn>
        <v-btn
          color="primary"
          text
          :disabled="!valid"
          @click="onClickAction"
        >
          {{ actionText }}
        </v-btn>
      </v-card-actions>
    </v-card>
  </v-dialog>
</template>

<script>
import ItemDialogCreateRequestFormPaidLeave from '../components/ItemDialogCreateRequestFormPaidLeave.vue'
import { mapState, mapActions, mapGetters } from 'vuex'
import { serverTimestamp } from '@firebase/firestore'
import { v4 as uuidv4} from 'uuid'

export default {
  name: 'ItemDialogRequestOverview',

  components: {
    ItemDialogCreateRequestFormPaidLeave
  },

  data () {
    return {
      /** 申請種別 */
      items: [
        {text: '休暇申請', value: 'paid_leave'},
        {text: '備品申請', value: 'equipment'},
      ],
      /** ダイアログの表示状態 */
      show: false,
      /** 入力したデータが有効かどうか */
      valid: false,
      /** 日付選択メニューの表示状態 */
      menu: false,
      /** 操作タイプ 'add' or 'edit' */
      actionType: 'add',
      /** 申請種別 */
      requestType: {},

      id: '',
      title: '',
      reason: '',
      date: '',
      contact: '',
      memo: '',
      itemName: '',

      /** バリデーションルール */
      requestTypeRules: [
        // v => v != '' || '申請種別は必須です',
      ],
      titleRules: [
        v => v.trim().length > 0 || 'タイトルは必須です',
      ],
      reasonRules: [
        v => v.trim().length > 0 || '事由は必須です',
      ],
      dateRules: [
        v => v.trim().length > 0 || '予定日時は必須です',
      ],
      contactRules: [
        v => v.trim().length > 0 || '緊急連絡先は必須です',
      ],
      itemNameRules: [
        v => v.trim().length > 0 || '商品名は必須です',
      ],
    }
  },

  computed: {
    ...mapState({
      userInfo: state => state.firestore.userInfo
    }),

    /** ダイアログのタイトル */
    titleText () {
      return this.actionType === 'add' ? 'データ追加' : 'データ編集'
    },
    /** ダイアログのアクション */
    actionText () {
      return this.actionType === 'add' ? '追加' : '更新'
    }
  },

  methods: {
    ...mapGetters({
      getUserEmail: 'firebase/getUserEmail',
      getArrayRoute: 'firestore/getArrayRoute',
    }),

    ...mapActions({
      batchAddSubCollectionsToUsers: 'firestore/batchAddSubCollectionsToUsers',
      fetchUserInfo: 'firestore/fetchUserInfo',
      createArrayRoute: 'firestore/createArrayRoute',
      sendEmail: 'firestore/sendEmail',
    }),

    /**
     * ダイアログを表示します。
     * このメソッドは親から呼び出されます。
     */
    open (actionType, item) {
      this.show = true
      this.actionType = actionType
      this.resetForm(item)
    },

    /** キャンセルがクリックされたとき */
    onClickClose () {
      this.show = false
    },

    /** 追加がクリックされたとき */
    async onClickAction () {
      if (this.actionType === 'add') {
        // 申請ルート情報をarray型に格納する
        // ↓ごちゃごちゃしてるのでキレイにする！
        const uid = uuidv4()
        const userId = this.getUserEmail()
        const requestTypeValue = this.requestType.value

        await this.fetchUserInfo({ userId })
        const department = this.userInfo.department
        await this.createArrayRoute({ requestTypeValue, department })
        const routes = this.getArrayRoute()[0]
        const arrayAddedStatus = []
        routes.approvers.forEach(element => {
          arrayAddedStatus.push({...element, status: '保留中'})
        })
        routes.approvers = arrayAddedStatus
        // ステップ数を格納する
        const currentStep = 1
        const maxStep = routes.approvers.length

        let item = {
          request: {
            request_type: this.requestType,
            title: this.title,
            status: '保留中',
            current_approver_email: routes.approvers[0].email,
            created_at: serverTimestamp(),
            email: this.userInfo.id,
            name: this.userInfo.name,
            department: this.userInfo.department
          },
          detail: {
            id: uid,
            request_type: this.requestType,
            title: this.title,
            status: '保留中',
            current_approver_email: routes.approvers[0].email,
            created_at: serverTimestamp(),
            email: this.userInfo.id,
            name: this.userInfo.name,
            department: this.userInfo.department,
            current_step: currentStep,
            max_step: maxStep,
            routes: routes,
            comments: []
          }
        }

        // itemオブジェクトに各申請に固有のプロパティを挿入する
        const createUniqueItem = (requestTypeValue) => {
          switch(requestTypeValue) {
            case 'paid_leave': {
              const uniqueItem = {
                reason: this.reason,
                date: this.date,
                contact: this.contact,
                memo: this.memo
              }
              return uniqueItem
            }
            case 'equipment': {
              const uniqueItem = {
                item_name: this.itemName,
                reason: this.reason,
                memo: this.memo
              }
              return uniqueItem
            }
          }
        }
        const uniqueItem = createUniqueItem(requestTypeValue)
        item.detail = { ...item.detail, ...uniqueItem }

        await this.batchAddSubCollectionsToUsers({ uid, userId, item })

        // // to: 承認者メールアドレスをセットする
        // const emailTo = routes.approvers[0].email
        // // subject: 申請が否認された旨を題名に記載する
        // const emailSubject = `[承認依頼] [${this.title}]`
        // // body: 詳細画面へのリンクを記載する
        // const detailPageUrl = `${window.location.href}others/requests/${uid}`
        // const emailBody = this.createEmailBody(emailSubject, detailPageUrl)
        // // メール送信
        // const emailConfig = { to: emailTo, subject: emailSubject, body: emailBody }
        // await this.sendEmail({ emailConfig })

        this.show = false
      }
    },

    /** フォームの内容を初期化します */
    resetForm (item = {}) {
      this.id = item.id || ''
      this.title = item.title || ''
      this.reason = item.reason || ''
      this.date = item.date || ''
      this.contact = item.contact || ''
      this.memo = item.memo || ''
      this.itemName = item.itemName || ''

      this.$refs.form.resetValidation()
    },

    createEmailBody(emailSubject, detailPageUrl) {
      const emailBody =
        `${emailSubject}<br><br>詳細は下記リンクからご確認ください。<br><br><a href="${detailPageUrl}">${detailPageUrl}</a>`
      return emailBody
    },

  }
}
</script>
